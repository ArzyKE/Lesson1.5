package com.company;

public class Boss {
    private int life;
    private int damage;
    private String lron;

    public int getLife() {
        return life;
    }

    public void setLife(int life) {
        this.life = life;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getLron() {
        return lron;
    }

    public void setLron(String lron) {
        this.lron = lron;
    }
}

